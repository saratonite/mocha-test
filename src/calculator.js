/**Calculator.js */

/**Add */

function add(a,b){

    return a+b;

}

/** Subtract  */

function subtract(a,b){

    return a-b;
}



/**Multiply */

function multiply(a,b) {

    return a*b;
}

/**Division */
function divide(a,b){

    return a/b;

}

module.exports = {
    add:add,
    subtract:subtract,
    multiply:multiply,
    divide:divide
}
